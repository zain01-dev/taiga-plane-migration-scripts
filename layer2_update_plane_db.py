INLINE_TOKEN_RE = re.compile(r"(\*\*.+?\*\*|\*[^*\n]+?\*|\[[^\]]+\]\([^)]+\))")
URL_RE = re.compile(r"(https?://[^\s<]+)")


def _split_urls(text):
    parts = []
    last = 0
    for m in URL_RE.finditer(text):
        if m.start() > last:
            parts.append(("text", text[last : m.start()]))
        parts.append(("url", m.group(1)))
        last = m.end()
    if last < len(text):
        parts.append(("text", text[last:]))
    return parts


def _parse_inline(text):
    nodes = []
    if not text:
        return nodes
    text = text.replace("\xa0", " ")
    idx = 0
    for m in INLINE_TOKEN_RE.finditer(text):
        if m.start() > idx:
            nodes.extend(_parse_plain(text[idx : m.start()]))
        token = m.group(1)
        if token.startswith("**") and token.endswith("**"):
            nodes.append({"type": "text", "text": token[2:-2], "marks": [{"type": "strong"}]})
        elif token.startswith("*") and token.endswith("*"):
            nodes.append({"type": "text", "text": token[1:-1], "marks": [{"type": "em"}]})
        elif token.startswith("["):
            match = re.match(r"\[([^\]]+)\]\(([^)]+)\)", token)
            if match:
                label, href = match.group(1), match.group(2)
                nodes.append(
                    {
                        "type": "text",
                        "text": label,
                        "marks": [{"type": "link", "attrs": {"href": href}}],
                    }
                )
            else:
                nodes.extend(_parse_plain(token))
        else:
            nodes.extend(_parse_plain(token))
        idx = m.end()
    if idx < len(text):
        nodes.extend(_parse_plain(text[idx:]))
    return nodes


def _parse_plain(text):
    nodes = []
    if not text:
        return nodes
    text = text.replace("\xa0", " ")
    for kind, part in _split_urls(text):
        if not part:
            continue
        if kind == "url":
            nodes.append(
                {"type": "text", "text": part, "marks": [{"type": "link", "attrs": {"href": part}}]}
            )
        else:
            nodes.append({"type": "text", "text": part})
    return nodes


def _paragraph_from_lines(lines):
    content = []
    for i, line in enumerate(lines):
        if i > 0:
            content.append({"type": "hardBreak"})
        content.extend(_parse_inline(line))
    return {"type": "paragraph", "attrs": {"textAlign": None}, "content": content or []}


def build_page_description_json(content):
    """Build a basic ProseMirror JSON doc from markdown-like text."""
    text = (content or "").strip()
    if not text:
        return {"type": "doc", "content": []}

    lines = text.splitlines()
    blocks = []
    i = 0
    while i < len(lines):
        raw_line = lines[i]
        line = raw_line.replace("\xa0", " ").rstrip("\r")
        if not line.strip():
            i += 1
            continue

        if line.startswith("```"):
            code_lines = []
            i += 1
            while i < len(lines) and not lines[i].startswith("```"):
                code_lines.append(lines[i].replace("\xa0", " ").rstrip("\r"))
                i += 1
            blocks.append({"type": "codeBlock", "content": [{"type": "text", "text": "\n".join(code_lines)}]})
            i += 1
            continue

        heading_match = re.match(r"^(#{1,6})\\s+(.*)$", line)
        if heading_match:
            level = len(heading_match.group(1))
            title = heading_match.group(2)
            blocks.append(
                {
                    "type": "heading",
                    "attrs": {"level": level, "textAlign": None},
                    "content": _parse_inline(title),
                }
            )
            i += 1
            continue

        list_match = re.match(r"^\s*([-*+])\s+(.*)$", line)
        if list_match:
            items = []
            while i < len(lines):
                m = re.match(r"^\s*([-*+])\s+(.*)$", lines[i].replace("\xa0", " ").rstrip("\r"))
                if not m:
                    break
                items.append(
                    {
                        "type": "listItem",
                        "content": [_paragraph_from_lines([m.group(2)])],
                    }
                )
                i += 1
            blocks.append({"type": "bulletList", "content": items})
            continue

        olist_match = re.match(r"^\s*\d+\.\s+(.*)$", line)
        if olist_match:
            items = []
            while i < len(lines):
                m = re.match(r"^\s*\d+\.\s+(.*)$", lines[i].replace("\xa0", " ").rstrip("\r"))
                if not m:
                    break
                items.append(
                    {
                        "type": "listItem",
                        "content": [_paragraph_from_lines([m.group(1)])],
                    }
                )
                i += 1
            blocks.append({"type": "orderedList", "content": items})
            continue

        if line.startswith("> "):
            quote_lines = []
            while i < len(lines) and lines[i].startswith("> "):
                quote_lines.append(lines[i][2:].replace("\xa0", " ").rstrip("\r"))
                i += 1
            blocks.append({"type": "blockquote", "content": [_paragraph_from_lines(quote_lines)]})
            continue

        para_lines = [line]
        i += 1
        while i < len(lines) and lines[i].replace("\xa0", " ").strip():
            para_lines.append(lines[i].replace("\xa0", " ").rstrip("\r"))
            i += 1
        blocks.append(_paragraph_from_lines(para_lines))

    return {"type": "doc", "content": blocks}


def build_page_description_html(content):
    """Build a simple HTML representation from markdown-like text."""
    doc = build_page_description_json(content)

    def render_nodes(nodes):
        out = []
        for node in nodes:
            if node.get("type") == "text":
                text = html.escape(node.get("text", ""))
                marks = node.get("marks") or []
                for mark in marks:
                    if mark.get("type") == "strong":
                        text = f"<strong>{text}</strong>"
                    elif mark.get("type") == "em":
                        text = f"<em>{text}</em>"
                    elif mark.get("type") == "link":
                        href = html.escape((mark.get("attrs") or {}).get("href", ""))
                        text = f"<a href=\"{href}\" target=\"_blank\" rel=\"noopener noreferrer\">{text}</a>"
                out.append(text)
            elif node.get("type") == "hardBreak":
                out.append("<br/>")
        return "".join(out)

    html_blocks = []
    for block in doc.get("content", []):
        btype = block.get("type")
        if btype == "paragraph":
            html_blocks.append(f"<p>{render_nodes(block.get('content') or [])}</p>")
        elif btype == "heading":
            level = (block.get("attrs") or {}).get("level", 1)
            level = max(1, min(6, int(level)))
            html_blocks.append(f"<h{level}>{render_nodes(block.get('content') or [])}</h{level}>")
        elif btype == "bulletList":
            items_html = []
            for item in block.get("content") or []:
                para = item.get("content") or []
                para_html = render_nodes((para[0].get("content") if para else []) or [])
                items_html.append(f"<li>{para_html}</li>")
            html_blocks.append(f"<ul>{''.join(items_html)}</ul>")
        elif btype == "orderedList":
            items_html = []
            for item in block.get("content") or []:
                para = item.get("content") or []
                para_html = render_nodes((para[0].get("content") if para else []) or [])
                items_html.append(f"<li>{para_html}</li>")
            html_blocks.append(f"<ol>{''.join(items_html)}</ol>")
        elif btype == "blockquote":
            para = block.get("content") or []
            para_html = render_nodes((para[0].get("content") if para else []) or [])
            html_blocks.append(f"<blockquote>{para_html}</blockquote>")
        elif btype == "codeBlock":
            code_text = ""
            if block.get("content"):
                code_text = block["content"][0].get("text", "")
            html_blocks.append(f"<pre><code>{html.escape(code_text)}</code></pre>")
    return "\n".join(html_blocks)


def get_project_page_context(cursor, project_id, fallback_user_ids):
    """Fetch project/workspace context and a safe owner fallback for pages."""
    cursor.execute(
        """
        SELECT workspace_id, created_by_id, project_lead_id, updated_by_id
        FROM projects
        WHERE id = %s
        """,
        (project_id,),
    )
    row = cursor.fetchone()
    if not row:
        return None

    workspace_id, created_by_id, project_lead_id, updated_by_id = row
    owner_id = created_by_id or project_lead_id or updated_by_id

    if not owner_id:
        for uid in fallback_user_ids:
            if uid:
                owner_id = uid
                break

    if not owner_id and workspace_id:
        cursor.execute(
            """
            SELECT member_id
            FROM workspace_members
            WHERE workspace_id = %s AND deleted_at IS NULL AND is_active = TRUE
            ORDER BY created_at ASC
            LIMIT 1
            """,
            (workspace_id,),
        )
        row = cursor.fetchone()
        if row:
            owner_id = row[0]

    return {
        "workspace_id": workspace_id,
        "owner_id": owner_id,
    }


