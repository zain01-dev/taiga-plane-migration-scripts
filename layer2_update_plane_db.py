def update_issue_activities(cursor, mapping):
    """Update issue activity actor and timestamps from Taiga metadata."""
    issues = mapping.get("issues", {})
    if not issues:
        return 0

    updated = 0
    print(f"\n🧭 Updating activity actors for {len(issues)} issues...")

    for info in issues.values():
        if not isinstance(info, dict):
            continue
        issue_id = info.get("plane_id")
        project_id = info.get("plane_project_id")
        if not issue_id or not project_id:
            continue

        created_at = info.get("created_at")
        author_email = info.get("author_email")
        last_activity_at = info.get("last_activity_at") or info.get("modified_at")
        last_activity_email = info.get("last_activity_email") or author_email

        author_id = lookup_user_id(cursor, author_email) if author_email else None
        last_actor_id = lookup_user_id(cursor, last_activity_email) if last_activity_email else None

        if author_id:
            cursor.execute(
                """
                UPDATE issue_activities
                SET actor_id = %s, created_by_id = %s, updated_by_id = %s,
                    created_at = COALESCE(%s, created_at), updated_at = COALESCE(%s, updated_at)
                WHERE issue_id = %s AND project_id = %s AND verb = 'created'
                """,
                (author_id, author_id, author_id, created_at, created_at, issue_id, project_id),
            )

        if last_actor_id:
            cursor.execute(
                """
                UPDATE issue_activities
                SET actor_id = %s, created_by_id = %s, updated_by_id = %s,
                    created_at = COALESCE(%s, created_at), updated_at = COALESCE(%s, updated_at)
                WHERE issue_id = %s AND project_id = %s AND verb = 'updated'
                """,
                (last_actor_id, last_actor_id, last_actor_id, last_activity_at, last_activity_at, issue_id, project_id),
            )

        updated += 1

    print(f"  ✅ Updated: {updated}")
    return updated


