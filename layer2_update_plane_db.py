def update_issues(cursor, mapping):
    """Update created_at, updated_at, and created_by_id for all migrated issues."""
    issues = mapping.get("issues", {})
    updated = 0
    skipped = 0
    issue_type_cache = {}
    project_workspace_cache = {}

    print(f"\n📝 Updating {len(issues)} issues...")

    for taiga_key, info in issues.items():
        if not isinstance(info, dict):
            skipped += 1
            continue

        plane_id = info.get("plane_id")
        created_at = info.get("created_at")
        modified_at = info.get("modified_at")
        author_email = info.get("author_email")

        if not plane_id:
            skipped += 1
            continue

        # Build dynamic UPDATE query
        set_clauses = []
        params = []

        if created_at:
            set_clauses.append("created_at = %s")
            params.append(created_at)

        if modified_at:
            set_clauses.append("updated_at = %s")
            params.append(modified_at)

        # Map Taiga issue type -> Plane issue type (DB)
        taiga_issue_type = info.get("taiga_issue_type")
        if taiga_issue_type and info.get("plane_project_id"):
            project_id = info.get("plane_project_id")
            workspace_id = get_project_workspace_id(cursor, project_id, project_workspace_cache)

            if workspace_id:
                key = (workspace_id, taiga_issue_type.strip().lower())
                issue_type_id = issue_type_cache.get(key)
                if not issue_type_id:
                    cursor.execute(
                        """
                        SELECT it.id
                        FROM issue_types it
                        WHERE it.workspace_id = %s AND it.deleted_at IS NULL
                          AND LOWER(it.name) = %s
                        LIMIT 1
                        """,
                        (workspace_id, taiga_issue_type.strip().lower()),
                    )
                    row = cursor.fetchone()
                    if row:
                        issue_type_id = row[0]
                    else:
                        issue_type_id = str(uuid4())
                        cursor.execute(
                            """
                            INSERT INTO issue_types (
                                created_at, updated_at, id, name, description, logo_props,
                                created_by_id, updated_by_id, workspace_id, is_active, deleted_at,
                                is_default, level, external_id, external_source, is_epic
                            ) VALUES (
                                NOW(), NOW(), %s, %s, '', %s::jsonb,
                                NULL, NULL, %s, TRUE, NULL,
                                FALSE, 0, %s, 'taiga', FALSE
                            )
                            """,
                            (issue_type_id, taiga_issue_type.strip(), json.dumps({}), workspace_id, taiga_issue_type),
                        )
                    issue_type_cache[key] = issue_type_id

                if issue_type_id:
                    cursor.execute(
                        """
                        SELECT id FROM project_issue_types
                        WHERE project_id = %s AND issue_type_id = %s AND deleted_at IS NULL
                        """,
                        (project_id, issue_type_id),
                    )
                    if not cursor.fetchone():
                        cursor.execute(
                            """
                            INSERT INTO project_issue_types (
                                created_at, updated_at, deleted_at, id, level, is_default, created_by_id,
                                issue_type_id, project_id, updated_by_id, workspace_id
                            ) VALUES (
                                NOW(), NOW(), NULL, %s, 0, FALSE, NULL,
                                %s, %s, NULL, %s
                            )
                            """,
                            (str(uuid4()), issue_type_id, project_id, workspace_id),
                        )
                    set_clauses.append("type_id = %s")
                    params.append(issue_type_id)

        story_points = info.get("story_points")
        if isinstance(story_points, (int, float)):
            try:
                point_val = int(round(float(story_points)))
                set_clauses.append("point = %s")
                params.append(point_val)
            except Exception:
                pass

