def to_json(val):
    if isinstance(val, str):
        return val
    return json.dumps(val or {})


def role_info_lookup(role_map, role_name):
    if not isinstance(role_map, dict):
        return None
    if not role_name:
        return None
    info = role_map.get(role_name)
    if isinstance(info, dict):
        return info
    key_lower = role_name.strip().lower()
    for key, val in role_map.items():
        if isinstance(key, str) and key.strip().lower() == key_lower and isinstance(val, dict):
            return val
    return None


def map_taiga_membership_role(role_name, is_admin, role_permissions):
    """Map Taiga membership role semantics to Plane role values."""
    if is_admin:
        return PLANE_ROLE_ADMIN

    normalized = (role_name or "").strip().lower()
    if normalized:
        if any(k in normalized for k in ("stakeholder", "guest", "viewer", "read only", "read-only")):
            return PLANE_ROLE_GUEST
        if any(k in normalized for k in ("owner", "admin", "manager", "lead", "product owner", "product-owner")):
            return PLANE_ROLE_MEMBER

    perms = set(role_permissions or [])
    if not perms:
        return PLANE_ROLE_MEMBER

    if any(p.startswith(("add_", "modify_", "delete_")) for p in perms):
        return PLANE_ROLE_MEMBER
    return PLANE_ROLE_GUEST


def build_taiga_membership_role_targets(mapping):
    """
    Build role targets from Taiga memberships.
    Returns dict: (email, project_id) -> {"role": int, "created_at": str|None}
    """
    targets = {}
    roles_by_project = mapping.get("taiga_roles") or {}

    for entry in mapping.get("taiga_memberships", []) or []:
        if not isinstance(entry, dict):
            continue
        email = normalize_email(entry.get("email") or entry.get("user"))
        project_id = entry.get("plane_project_id")
        if not email or not project_id:
            continue

        role_name = entry.get("role")
        role_info = role_info_lookup(roles_by_project.get(project_id), role_name)
        perms = role_info.get("permissions", []) if role_info else []
        mapped_role = map_taiga_membership_role(role_name, bool(entry.get("is_admin", False)), perms)
        created_at = entry.get("created_at")
        key = (email, project_id)
        current = targets.get(key)
        if current is None:
            targets[key] = {"role": mapped_role, "created_at": created_at}
        else:
            # Keep strongest role and earliest known membership timestamp.
            strongest = max(int(current["role"]), int(mapped_role))
            current_created = current.get("created_at")
            earliest = current_created
            if created_at and (not earliest or created_at < earliest):
                earliest = created_at
            targets[key] = {"role": strongest, "created_at": earliest}

    return targets


def build_workspace_membership_created_targets(mapping, project_workspace_lookup):
    """
    Build earliest known Taiga membership created_at per (email, workspace_id).
    Returns dict: (email, workspace_id) -> created_at
    """
    targets = {}
    for entry in mapping.get("taiga_memberships", []) or []:
        if not isinstance(entry, dict):
            continue
        email = normalize_email(entry.get("email") or entry.get("user"))
        project_id = entry.get("plane_project_id")
        created_at = entry.get("created_at")
        if not email or not project_id or not created_at:
            continue
        workspace_id = get_project_workspace_id(None, project_id, project_workspace_lookup)
        if not workspace_id:
            continue
        key = (email, workspace_id)
        current = targets.get(key)
        if not current or created_at < current:
            targets[key] = created_at
    return targets


def collect_email_project_map(mapping):
    """Return dict: email -> set(project_id)."""
    email_projects = {}

    def add(email, project_id):
        if not email or "@" not in email:
            return
        email_projects.setdefault(email, set())
        if project_id:
            email_projects[email].add(project_id)

    for info in (mapping.get("issues") or {}).values():
        if not isinstance(info, dict):
            continue
        project_id = info.get("plane_project_id")
        add(info.get("author_email"), project_id)
        add(info.get("assigned_to_email"), project_id)
        for watcher in info.get("watchers_emails", []) or []:
            add(watcher, project_id)

    for info in (mapping.get("comments") or {}).values():
        if not isinstance(info, dict):
            continue
        add(info.get("author_email"), info.get("plane_project_id"))

    for info in (mapping.get("modules") or {}).values():
        if not isinstance(info, dict):
            continue
        add(info.get("author_email"), info.get("plane_project_id"))

    for info in (mapping.get("cycles") or {}).values():
        if not isinstance(info, dict):
            continue
        add(info.get("author_email"), info.get("plane_project_id"))

    for info in (mapping.get("pages") or {}).values():
        if not isinstance(info, dict):
            continue
        add(info.get("author_email"), info.get("plane_project_id"))
        add(info.get("last_modifier_email"), info.get("plane_project_id"))

    for info in (mapping.get("attachments") or {}).values():
        if not isinstance(info, dict):
            continue
        add(info.get("author_email"), info.get("plane_project_id"))

    for membership in mapping.get("taiga_memberships", []) or []:
        if not isinstance(membership, dict):
            continue
        add(membership.get("email") or membership.get("user"), membership.get("plane_project_id"))

    for email in mapping.get("taiga_emails", []) or []:
        add(email, None)

    return email_projects


