    project_workspace = {}
    for info in (mapping.get("projects") or {}).values():
        if not isinstance(info, dict):
            continue
        pid = info.get("plane_id")
        if not pid:
            continue
        workspace_id = get_project_workspace_id(cursor, pid, project_workspace)
        if workspace_id:
            project_workspace[pid] = workspace_id

